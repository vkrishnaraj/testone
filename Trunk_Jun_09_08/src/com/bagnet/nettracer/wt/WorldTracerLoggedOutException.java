package com.bagnet.nettracer.wt;

public class WorldTracerLoggedOutException extends WorldTracerException {

	public WorldTracerLoggedOutException() {
	}

	public WorldTracerLoggedOutException(String message) {
		super(message);
	}

	public WorldTracerLoggedOutException(Throwable cause) {
		super(cause);
	}

	public WorldTracerLoggedOutException(String message, Throwable cause) {
		super(message, cause);
	}

}
