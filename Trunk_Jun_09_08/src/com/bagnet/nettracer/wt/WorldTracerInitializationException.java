package com.bagnet.nettracer.wt;

public class WorldTracerInitializationException extends WorldTracerException {

	public WorldTracerInitializationException() {
	}

	public WorldTracerInitializationException(String message) {
		super(message);
	}

	public WorldTracerInitializationException(Throwable cause) {
		super(cause);
	}

	public WorldTracerInitializationException(String message, Throwable cause) {
		super(message, cause);
	}

}
