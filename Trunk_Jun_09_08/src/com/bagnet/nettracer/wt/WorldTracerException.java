package com.bagnet.nettracer.wt;

public class WorldTracerException extends Exception {

	public WorldTracerException() {
	}

	public WorldTracerException(String message) {
		super(message);
	}

	public WorldTracerException(Throwable cause) {
		super(cause);
	}

	public WorldTracerException(String message, Throwable cause) {
		super(message, cause);
	}

}
