package com.bagnet.nettracer.wt.utils;

public class ActionFileDto {
	int itemNumber;
	String summary;
	String details;
	String ahlId;
	String ohdId;
	double percentMatch;
	
	public int getItemNumber() {
		return itemNumber;
	}
	public void setItemNumber(int itemNumber) {
		this.itemNumber = itemNumber;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}
	public String getAhlId() {
		return ahlId;
	}
	public void setAhlId(String ahlId) {
		this.ahlId = ahlId;
	}
	public String getOhdId() {
		return ohdId;
	}
	public void setOhdId(String ohdId) {
		this.ohdId = ohdId;
	}
	public double getPercentMatch() {
		return percentMatch;
	}
	public void setPercentMatch(double percentMatch) {
		this.percentMatch = percentMatch;
	}
	
	
}
