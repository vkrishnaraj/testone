package com.bagnet.nettracer.other;

public class JRMaxFilesException extends RuntimeException {

	public JRMaxFilesException() {
	}

	public JRMaxFilesException(String message) {
		super(message);
	}

	public JRMaxFilesException(Throwable cause) {
		super(cause);
	}

	public JRMaxFilesException(String message, Throwable cause) {
		super(message, cause);
	}

}
