package com.bagnet.nettracer.tracing.utils;

public class IncidentCount {
	int incidentTotalCount = 0;
	int incidentTypeDelayedCount = 0;
	int incidentTypePilferageCount = 0;
	int incidentTypeDamagedCount = 0;
	
	public int getIncidentTotalCount() {
		return incidentTotalCount;
	}
	public void setIncidentTotalCount(int incidentTotalCount) {
		this.incidentTotalCount = incidentTotalCount;
	}

	public int getIncidentTypeDelayedCount() {
		return incidentTypeDelayedCount;
	}
	public void setIncidentTypeDelayedCount(int incidentTypeDelayedCount) {
		this.incidentTypeDelayedCount = incidentTypeDelayedCount;
	}
	public int getIncidentTypePilferageCount() {
		return incidentTypePilferageCount;
	}
	public void setIncidentTypePilferageCount(int incidentTypePilferageCount) {
		this.incidentTypePilferageCount = incidentTypePilferageCount;
	}
	public int getIncidentTypeDamagedCount() {
		return incidentTypeDamagedCount;
	}
	public void setIncidentTypeDamagedCount(int incidentTypeDamagedCount) {
		this.incidentTypeDamagedCount = incidentTypeDamagedCount;
	}
	
	

}
